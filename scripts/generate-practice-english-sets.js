const fs = require("fs");
const path = require("path");
const vm = require("vm");

const repoRoot = path.resolve(__dirname, "..");
const sourcePath = "C:/Users/mhdzh/.codex/attachments/8d0567b3-0279-475c-863e-336d4a880061/pasted-text.txt";
const outPath = path.join(repoRoot, "js", "practice-english-sets.js");

function fixText(value) {
  return String(value ?? "")
    .replace(/â€”/g, "-")
    .replace(/â€“/g, "-")
    .replace(/â†’/g, "->")
    .replace(/â€˜|â€™/g, "'")
    .replace(/â€œ|â€�/g, '"')
    .replace(/Â/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function fixHtml(value) {
  return String(value ?? "")
    .replace(/â€”/g, "-")
    .replace(/â€“/g, "-")
    .replace(/â†’/g, "->")
    .replace(/â€˜|â€™/g, "'")
    .replace(/â€œ|â€�/g, '"')
    .replace(/Â/g, "")
    .trim();
}

function extractSets() {
  const html = fs.readFileSync(sourcePath, "utf8");
  const start = html.indexOf("const SETS = [");
  const end = html.indexOf("/* ============ LOGIC", start);
  if (start === -1 || end === -1) {
    throw new Error("Tidak dapat mencari blok const SETS dalam lampiran English.");
  }

  const script = `${html.slice(start, end)}\nglobalThis.__SETS__ = SETS;`;
  const context = {};
  vm.createContext(context);
  vm.runInContext(script, context, { timeout: 10000 });

  if (!Array.isArray(context.__SETS__)) {
    throw new Error("Blok SETS tidak berjaya diekstrak.");
  }
  return context.__SETS__;
}

function normalizeStimulus(html) {
  const cleaned = fixHtml(html);
  if (!cleaned) return "";
  return `<div class="english-stimulus">${cleaned}</div>`;
}

function normalizeQuestion(item, setNo, questionNo, group, stimulus) {
  const options = Array.isArray(item.o) ? item.o.map(fixText) : [];
  const answer = Number(item.a);
  const q = fixText(item.q);
  const explain = fixText(item.e) || `Answer: ${"ABCD"[answer]}.`;

  if (!q) throw new Error(`Set ${setNo} soalan ${questionNo}: teks soalan kosong.`);
  if (options.length < 2 || options.length > 4 || options.some((option) => !option)) {
    throw new Error(`Set ${setNo} soalan ${questionNo}: pilihan jawapan tidak lengkap.`);
  }
  if (!Number.isInteger(answer) || answer < 0 || answer >= options.length) {
    throw new Error(`Set ${setNo} soalan ${questionNo}: jawapan tidak sah.`);
  }

  return {
    sourceId: `E${String(setNo).padStart(2, "0")}-Q${String(questionNo).padStart(2, "0")}`,
    q,
    options,
    answer,
    category: "English",
    level: group ? fixText(group) : "English PKSK",
    explain,
    ...(stimulus ? { fig: stimulus } : {}),
  };
}

function buildEnglishSets() {
  const sets = extractSets();
  if (sets.length !== 10) throw new Error(`Jumlah set English perlu 10, dijumpai ${sets.length}.`);

  const output = {};
  const seenIds = new Set();
  let total = 0;

  sets.forEach((set, setIndex) => {
    const setNo = setIndex + 1;
    let group = "";
    let stimulus = "";
    let questionNo = 0;
    const questions = [];

    for (const item of set.items || []) {
      if (item.h) {
        group = item.h;
        stimulus = "";
        continue;
      }
      if (item.s) {
        stimulus = normalizeStimulus(item.s);
        continue;
      }
      if (item.q) {
        questionNo += 1;
        const normalized = normalizeQuestion(item, setNo, questionNo, group, stimulus);
        if (seenIds.has(normalized.sourceId)) throw new Error(`Duplikasi ID: ${normalized.sourceId}`);
        seenIds.add(normalized.sourceId);
        questions.push(normalized);
      }
    }

    if (questions.length !== 20) {
      throw new Error(`Set ${setNo} perlu ada 20 soalan English, dijumpai ${questions.length}.`);
    }
    output[setNo] = questions;
    total += questions.length;
  });

  if (total !== 200) throw new Error(`Jumlah soalan English perlu 200, dijumpai ${total}.`);
  return { output, total };
}

function main() {
  const { output, total } = buildEnglishSets();
  const generated = `// Auto-generated by scripts/generate-practice-english-sets.js\n` +
    `// Source: ${sourcePath}\n` +
    `(function () {\n` +
    `  const englishSets = ${JSON.stringify(output, null, 2)};\n\n` +
    `  window.PKSK_SET_QUESTIONS = window.PKSK_SET_QUESTIONS || {};\n` +
    `  Object.entries(englishSets).forEach(([setNo, questions]) => {\n` +
    `    window.PKSK_SET_QUESTIONS[setNo] = {\n` +
    `      ...(window.PKSK_SET_QUESTIONS[setNo] || {}),\n` +
    `      english: questions,\n` +
    `    };\n` +
    `  });\n` +
    `})();\n`;

  fs.writeFileSync(outPath, generated, "utf8");
  console.log(`Generated ${path.relative(repoRoot, outPath)} with ${total} English questions.`);
  console.log(Object.entries(output).map(([setNo, questions]) => `Set ${setNo}: ${questions.length}`).join(", "));
}

main();
